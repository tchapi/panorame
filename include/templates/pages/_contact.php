  <h3>API</h3>
  
  <p>Panorame offers an API which allow developers to embed the Panorame experience into their web services.</p>
  
  <p>For web services dedicated to recommending places, landmarks, addresses, it contributes to significantly enhance the end user experience. It allows the user to focus the search on his Panorame playzone, to make it very visual and dynamic.</p>
  
  <p>The Panorame API includes:
      - the Playzone definition and visualization module;
      - a function to query if a location is part of the current playzone;
      - customizable search fields.
  </p>

